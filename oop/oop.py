class animal:
	def __init__(self,name,color,age,size):
		self.name=name
		self.color=color
		self.age=age
		self.size=size
	def printall(self):
		print (self.name)
		print (self.color)
		print (self.age)
		print (self.size)

dog=animal(name="joe",color="yellow",age=100,size="huge")
cat=animal(name="max",color="green",age=50,size="small")

dog.printall()
print ("       ")
cat.printall()
